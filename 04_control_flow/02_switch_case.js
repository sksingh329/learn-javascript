const month = 3
let month_name = ""

switch(month){
    case 1:
        month_name = "January"
        break;
    case 2:
        month_name = "February"
        break;
    case 3:
        month_name = "March"
        break;
    case 4:
        month_name = "April"
        break;
}

console.log(month_name);